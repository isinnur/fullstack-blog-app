const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },

  //? createdAt ve updatedAt olmak Ã¼zere ekstra 2 alan ekler
  { timestamps: true }
);

module.exports = mongoose.model("User", UserSchema);
